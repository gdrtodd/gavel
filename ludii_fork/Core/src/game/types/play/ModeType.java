/**
 * Defines the possible modes of play.
 * 
 * @author cambolbro and Eric.Piette
 */
public enum ModeType
{
		/** Players alternate making discrete moves. */
		Alternating,
		
		/** Players move at the same time. */
		Simultaneous,
		
		/** Simulation game */
		Simulation,
	}
